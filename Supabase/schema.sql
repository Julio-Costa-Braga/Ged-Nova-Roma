-- Política: admins podem inserir/editar/deletar
-- Policies: allow create/update/delete only when the acting user
-- has 'financeiro' or 'ambos' access in the `usuarios` table.
-- NOTE: PostgreSQL policy syntax uses `WITH CHECK` for INSERT and
-- `USING` for SELECT/UPDATE/DELETE. The original `with exists` was
-- invalid SQL (caused the syntax error). Adapt the subquery to your
-- auth linkage (e.g. join on auth.uid() or username) if needed.

create policy "Insert admin" on documentos_ged
  for insert with check (exists (select 1 from usuarios where acesso in ('financeiro', 'ambos')));

create policy "Update admin" on documentos_ged
  for update using (exists (select 1 from usuarios where acesso in ('financeiro', 'ambos')));

create policy "Delete admin" on documentos_ged
  for delete using (exists (select 1 from usuarios where acesso in ('financeiro', 'ambos')));

