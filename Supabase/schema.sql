create index if not exists idx_movimentacoes_colaborador on movimentacoes_rh(colaborador_id);
create index if not exists idx_contratos_colaborador on contratos(colaborador_id);
create index if not exists idx_documentos_setor on documentos_ged(setor);
create index if not exists idx_documentos_tipo on documentos_ged(tipo_documento);
create index if not exists idx_transacoes_data on transacoes_financeiras(data);
create index if not exists idx_anexos_documento on anexos_documentos(documento_id);

