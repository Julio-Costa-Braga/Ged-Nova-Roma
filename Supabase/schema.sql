-- Habilita pgcrypto para hashing (bcrypt via crypt/gen_salt)
CREATE EXTENSION IF NOT EXISTS pgcrypto;

-- Função auxiliar: cria um usuário com hash bcrypt no banco
CREATE OR REPLACE FUNCTION create_user(
  p_nome text,
  p_username text,
  p_password text,
  p_acesso text
)
RETURNS TABLE(id bigint, nome text, username text, acesso text, created_at timestamptz)
LANGUAGE plpgsql AS $$
BEGIN
  IF p_acesso NOT IN ('financeiro','rh','ambos','apenas-observador') THEN
    RAISE EXCEPTION 'acesso_invalido';
  END IF;

  IF EXISTS(SELECT 1 FROM usuarios WHERE username = p_username) THEN
    RAISE EXCEPTION 'username_ja_existe';
  END IF;

  RETURN QUERY
    INSERT INTO usuarios(nome, username, senha, acesso, must_change_password)
    VALUES (p_nome, p_username, crypt(p_password, gen_salt('bf')), p_acesso, true)
    RETURNING id, nome, username, acesso, must_change_password, created_at;
END;
$$;
