-- 1. USUÁRIOS
create table if not exists usuarios (
  id bigint generated by default as identity primary key,
  nome text not null,
  username text not null unique,
  senha text not null,
  acesso text not null check (acesso in ('financeiro', 'rh', 'ambos', 'apenas-observador')),
  must_change_password boolean default false,
  created_at timestamptz default now()
);

-- 2. COLABORADORES (RH)
create table if not exists colaboradores (
  id bigint generated by default as identity primary key,
  nome text not null,
  funcao text default '',
  situacao text default 'Ativo' check (situacao in ('Ativo', 'Inativo')),
  admissao date,
  demissao date,
  matricula text default '',
  localizacao text default '',
  unidade text default '',
  data_nasc date,
  remuneracao numeric(12,2) default 0,
  telefone text default '',
  rg text default '',
  cpf text default '',
  pis text default '',
  sexo text default '',
  pai_mae text default '',
  titulacao text default '',
  curso text default '',
  email_pessoal text default '',
  email_corporativo text default '',
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

-- 3. PASTAS DE DOCUMENTOS (RH)
create table if not exists pastas_documentos (
  id bigint generated by default as identity primary key,
  colaborador_id bigint not null references colaboradores(id) on delete cascade,
  categoria text not null,
  nome_arquivo text not null,
  tamanho text default '',
  data_upload date default current_date,
  status text default 'Enviado',
  tipo text default '',
  observacao text default '',
  created_at timestamptz default now()
);

-- 4. MOVIMENTAÇÕES RH
create table if not exists movimentacoes_rh (
  id bigint generated by default as identity primary key,
  colaborador_id bigint not null references colaboradores(id) on delete cascade,
  categoria text not null,
  nome_arquivo text not null,
  tamanho text default '',
  data_upload date default current_date,
  created_at timestamptz default now()
);

-- 5. CONTRATOS / DOCUMENTAÇÕES ENVIADAS
create table if not exists contratos (
  id bigint generated by default as identity primary key,
  colaborador_id bigint not null references colaboradores(id) on delete cascade,
  tipo text not null,
  nome_arquivo text not null,
  observacao text default '',
  destinatarios jsonb default '[]',
  status text default 'Pendente Assinatura',
  created_at timestamptz default now()
);

-- 6. DOCUMENTOS GED (Captura & Classificação)
create table if not exists documentos_ged (
  id bigint generated by default as identity primary key,
  tipo_documento text not null,
  setor text not null,
  cpf text default '',
  cnpj text default '',
  valor numeric(12,2) default 0,
  tipo_financeiro text default '' check (tipo_financeiro in ('', 'Receita', 'Despesa')),
  data_envio date default current_date,
  data_vencimento date,
  numero_nota text default '',
  tags text[] default '{}',
  codigo_siga text default '',
  data_validade date,
  lembrete_dias int default 0,
  email_assinante text default '',
  nome_arquivo text not null,
  url_arquivo text default '',
  status text default 'Pendente',
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

-- 7. TRANSAÇÕES FINANCEIRAS (Fluxo de Caixa)
create table if not exists transacoes_financeiras (
  id bigint generated by default as identity primary key,
  data date not null default current_date,
  descricao text not null,
  centro_custo text not null,
  tipo text not null check (tipo in ('Receita', 'Despesa')),
  valor numeric(12,2) not null default 0,
  created_at timestamptz default now()
);

-- 8. ANEXOS DE DOCUMENTOS GED
create table if not exists anexos_documentos (
  id bigint generated by default as identity primary key,
  documento_id bigint not null references documentos_ged(id) on delete cascade,
  nome_arquivo text not null,
  url_arquivo text not null,
  tamanho text default '',
  created_at timestamptz default now()
);

-- ============================================
-- ÍNDICES
-- ============================================
