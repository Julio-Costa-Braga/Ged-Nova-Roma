-- E.0 - Tabela de documentos financeiros
CREATE TABLE IF NOT EXISTS documentos (
  id bigint generated by default as identity primary key,
  tipo_doc text not null,
  setor text not null,
  identificador text not null unique,
  valor numeric(12,2) default 0,
  tipo_financeiro text not null check (tipo_financeiro in ('Receita', 'Despesa')),
  data_envio date default current_date,
  status_assinatura text default 'Pendente Assinatura',
  tags text default '',
  numero_nf text default '',
  created_at timestamptz default now()
);

