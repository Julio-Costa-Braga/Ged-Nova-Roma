-- H.1 - Listar todos os usuários com permissões
SELECT 
  id,
  nome,
  username,
  acesso,
  CASE 
    WHEN acesso = 'ambos' THEN 'Acesso Completo'
    WHEN acesso = 'financeiro' THEN 'Apenas Financeiro'
    WHEN acesso = 'rh' THEN 'Apenas RH'
    ELSE 'Apenas Observação'
  END as permissao_descrita,
  created_at
FROM usuarios
ORDER BY nome;

-- H.2 - Colaboradores por departamento
SELECT 
  unidade,
  COUNT(*) as total,
  COUNT(CASE WHEN situacao = 'Ativo' THEN 1 END) as ativos,
  COUNT(CASE WHEN situacao = 'Inativo' THEN 1 END) as inativos,
  ROUND(AVG(remuneracao), 2) as salario_medio
FROM colaboradores
GROUP BY unidade
ORDER BY total DESC;

-- H.3 - Documentos com status pendente
SELECT 
  id,
  tipo_doc,
  setor,
  valor,
  data_envio,
  numero_nf,
  status_assinatura
FROM documentos
WHERE status_assinatura = 'Pendente Assinatura'
ORDER BY data_envio DESC;

-- H.4 - Resumo financeiro do mês
SELECT 
  DATE_TRUNC('month', data_envio)::DATE as mes,
  tipo_financeiro,
  COUNT(*) as documentos,
  ROUND(SUM(valor), 2) as valor_total
FROM documentos
WHERE DATE_TRUNC('month', data_envio) = DATE_TRUNC('month', CURRENT_DATE)
GROUP BY DATE_TRUNC('month', data_envio), tipo_financeiro
ORDER BY mes DESC;

-- H.5 - Próximos aniversariantes (RH)
SELECT 
  nome,
  data_nasc,
  EXTRACT(MONTH FROM data_nasc) as mes,
  EXTRACT(DAY FROM data_nasc) as dia,
  CASE 
    WHEN EXTRACT(MONTH FROM data_nasc) = EXTRACT(MONTH FROM CURRENT_DATE) THEN 'Este Mês'
    WHEN EXTRACT(MONTH FROM data_nasc) = EXTRACT(MONTH FROM CURRENT_DATE + INTERVAL '1 month') THEN 'Próximo Mês'
    ELSE 'Futuro'
  END as quando
FROM colaboradores
WHERE situacao = 'Ativo'
ORDER BY EXTRACT(MONTH FROM data_nasc), EXTRACT(DAY FROM data_nasc);

